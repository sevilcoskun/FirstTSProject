// Arithmetics
var x = 10;
var y = 20;
console.log("Arithmetic Operators -> sum(+) : ", (x + y));
console.log("Arithmetic Operators -> subtraction(-) : ", (y - x));
console.log("Arithmetic Operators -> multiplication(*) : ", (x * y));
console.log("Arithmetic Operators -> division(/) : ", (y / x));
console.log("Arithmetic Operators -> reminder(%) : ", (y % x));
// Assigning and compute
var a = 10;
console.log("Assigning Operators -> sum(+=) : ", (a += 5));
console.log("Assigning Operators -> subtraction(-=) : ", (a -= 5));
console.log("Assigning Operators -> multiplication(*=) : ", (a *= 5));
console.log("Assigning Operators -> division(/=) : ", (a /= 5));
console.log("Assigning Operators -> reminder(%=) : ", (a %= 5));
// Comparison
var v1 = 10;
console.log("Comparison Operators -> 10===10 : ", v1 === 10);
console.log("Comparison Operators -> 10!==10 : ", v1 !== 10);
console.log("Comparison Operators -> 10<10 : ", v1 < 10);
console.log("Comparison Operators -> 10>10 : ", v1 > 10);
console.log("Comparison Operators -> 10>=10 : ", v1 <= 10);
console.log("Comparison Operators -> 10<=10 : ", v1 >= 10);
// Logical
var t = true;
var f = false;
console.log("Logical Operators -> (t && f) : ", (t && f));
console.log("Logical Operators -> (t || f) : ", (t || f));
console.log("Logical Operators -> !t : ", !t);
// Ternary Operators
console.log("Ternary Operator -> t ? 'true' : 'false' : ", (t ? "true" : "false"));
